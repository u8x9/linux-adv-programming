#!/usr/bin/bash

echo "\$0 = $0"
echo "\$1 = $1"
echo "\$2 = $2"
echo "\$3 = $3"
echo "\$4 = $4"
echo "\$5 = $5"
echo "\$6 = $6"
echo "\$7 = $7"
echo "\$8 = $8"
echo "\$9 = $9"
echo "\$10 = ${10}"

# `$#` 参数个数，不包括 `$0`
echo "参数个数(\$#) = $#"

# `$@` 或 `$*` 所有参数，不包括 `$0`
echo "所有参数(\$@) = $@"
echo "所有参数(\$*) = $*"

# `$$` 当前进程的进程号
echo "当前进程的进程号(\$\$) = $$"

# `$?` 成功：0；失败：非0
echo "上条命令执行结果(\$?) = $?"